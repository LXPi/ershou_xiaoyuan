package com.xiaoping.servlet;

public class Appidata {
        private int ids;
        private String date;
        private String dname;
        private String dvxname;
        private float price;
        private float yprice;
        private String lei;
        private int wan;

    public int getIds() {
        return ids;
    }

    public void setIds(int ids) {
        this.ids = ids;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public String getDvxname() {
        return dvxname;
    }

    public void setDvxname(String dvxname) {
        this.dvxname = dvxname;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public float getYprice() {
        return yprice;
    }

    public void setYprice(float yprice) {
        this.yprice = yprice;
    }

    public String getLei() {
        return lei;
    }

    public void setLei(String lei) {
        this.lei = lei;
    }

    public int getWan() {
        return wan;
    }

    public void setWan(int wan) {
        this.wan = wan;
    }
}
